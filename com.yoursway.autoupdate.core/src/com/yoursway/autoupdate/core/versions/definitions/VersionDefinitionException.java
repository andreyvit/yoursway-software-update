package com.yoursway.autoupdate.core.versions.definitions;

public class VersionDefinitionException extends Exception {

	private static final long serialVersionUID = 1L;

	public VersionDefinitionException() {
		super();
	}

	public VersionDefinitionException(String message, Throwable cause) {
		super(message, cause);
	}

	public VersionDefinitionException(String message) {
		super(message);
	}

	public VersionDefinitionException(Throwable cause) {
		super(cause);
	}

}
